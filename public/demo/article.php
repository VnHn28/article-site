                            <h1 class="article-title">伊隆・馬斯克眼中的人類未來：基本收入、生化人、自動駕駛和地下城</h1>
                            <img class="article-cover" src="img/tmp/demo01.png">
                            <p class="article-lead">太空探索技術公司 SpaceX 和特斯拉電動汽車公司 CEO 伊隆 · 馬斯克（Elon Musk）近日在杜拜出席了世界政府首腦會議（World Government Summit），並上台做了演講。這位億萬富翁表達了他對人工智慧、全民基本收入和人類未來等的看法。下面是他對人類未來所作出的四大預測：</p>
                        </header>
                        <main class="article-editor">
                            <h2>1. 全民基本收入將解決自動化帶來的經濟問題</h2>
                            <p>馬斯克說：「在未來，人類比機器人表現更出色的工作將越來越少。我想澄清一下，這不是我希望發生的事情，而是實際上很可能會發生的事情。如果我的估計沒錯，那麼這一切是很有可能發生的。因此，我們必須思考到底該如何應對。我想，推行全民基本收入是必須的。在未來，商品和服務的供應量將會變得極其豐富。伴隨自動化而來的是豐裕的生活。幾乎所有東西都變得非常便宜了。我們最終將要實現全民基本收入，這是必須的。我們面臨的更大挑戰在於：人類將如何獲得人生的意義？現在，很多人從工作中找到了人生的意義。在將來，如果社會已不需要你工作，那麼你的生活意義是什麼呢？你會覺得自己一無是處嗎？這是一個更棘手的問題。」</p>
                            <h2>2. 人類大腦需要與數位智慧結合</h2>
                            <p>馬斯克稱，在交流過程中，人類用手指輸入文字的速度是每秒 10 位元，而電腦的速度則高達 「每秒上兆位元」。因此，人腦需要與高科技結合才能跟上步伐。</p>
                            <p>「隨著時間的推移，我們將可能會看到生物智慧與數位智慧進行更緊密的融合。這實際上就是頻寬的問題，即你的大腦與數位智慧之間的連接速度，尤其是輸出的速度。如果大腦擁有較高頻寬的界面，那麼它將幫助人類實現和機器智慧之間的共生，並可能解決人類控制和實用性的問題。」 他說。</p>
                            <h2>3. 無人駕駛汽車在未來十年內將會普及</h2>
                            <p>馬斯克稱，特斯拉無人駕駛技術的安全性可能已超過了人類司機。</p>
                            <p>「我預計，在未來十年內，不採用無人駕駛技術的汽車將會變得非常罕見。現在，全球有大約 20 億輛汽車，總年產量約為 1 億輛。一輛汽車的平均使用壽命約為 20-25 年。」 他說，「現在無人駕駛汽車剛剛出現，對於社會的影響並不大；但是，等到夠多的無人駕駛汽車出現的時候，它就會對人類社會產生巨大影響。」</p>
                            <h2>4. 未來城市將位於地下</h2>
                            <p>有人問馬斯克為何要在 SpaceX 公司位於洛杉磯的總部周圍挖掘地下隧道，他先說這是一個 「秘密」，然後又描繪了他想象中的未來城市的樣子。</p>
                            <p>「我認為解決城市人口擁擠的辦法就是在城市下面建立地下隧道網路。儘管這種觀點現在看起來微不足道，或者有點傻。事實上，我多年來一直持有這種觀點。現在的挑戰就是：你如何快速、安全、低成本地挖掘隧道？如果隧道挖掘技術得到改善，那麼你就可以更快、更安全、更廉價地挖掘隧道，進而徹底解決城市面臨的交通問題，」 他說。</p>
